let d = new Date();
console.log(d);  // Current date and time
console.log(d.getDate());  // Day of the month (1-31)
console.log((d.getMonth())+1);  // Month (0-11)
console.log(d.getFullYear());  // Full year (e.g., 2024)

console.log(d.getHours());  // Current hour (0-23)
console.log(d.getMinutes());  // Current minutes (0-59)
console.log(d.getSeconds());  // Current seconds (0-59)

console.log(d.getHours() + ":" + d.getMinutes() + ":" + d.getSeconds());  // Current time in HH:MM:SS format


