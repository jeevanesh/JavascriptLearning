let x = 10;
let y = 20;

// Arithmetic Operators
let sum = x + y;
let difference = y - x;
let product = x * y;
let quotient = y / x;
let remainder = y % x;
let increment = ++x;    // Pre-increment
let decrement = --y;    // Pre-decrement    
let postIncrement = x++; // Post-increment
let postDecrement = y--; // Post-decrement
document.write("Sum: " + sum + "<br>");
document.write("Difference: " + difference + "<br>");
document.write("Product: " + product + "<br>"); 
document.write("Quotient: " + quotient + "<br>");
document.write("Remainder: " + remainder + "<br>");
document.write("Increment: " + increment + "<br>");
document.write("Decrement: " + decrement + "<br>");
document.write("Post Increment: " + postIncrement + "<br>");
document.write("Post Decrement: " + postDecrement + "<br>");
